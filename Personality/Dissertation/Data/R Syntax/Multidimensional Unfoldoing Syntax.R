#Using Terminal, activate "wine" (i.e., the windows emulator). To do this:
#1. Open terminal.
#2. Type "wine" and press "enter".
#3. Type "cd ~/Documents/GGUM" and press "enter" (Note: If you change the location of the GGUM files, this will need to be changed as well).
#4. Type "wine GGUM.EXE"
#5. When executing files, you will need to change the location of the files in GGUM2004.
### You shoud now be simulating GGUM in Mac.

